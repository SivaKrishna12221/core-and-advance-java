package prepInstaStrings;

public class VowelOrConsonant 
{
  public static void main(String[] args)
  {
	char c='m';
	if(c=='A'||c=='E'||c=='I'||c=='O'||c=='U'||c=='a'||c=='e'||c=='i'||c=='o'||c=='u')
	{
		System.out.println(c+ " is a vowel");
	}
	else
	{
		System.out.println(c+" is consonant");
	}
}
}
