package javatPointControlStatements;

public class ClassForLoop 
/*
 * forloop:
 * we use the for loop only when we exactly known the
 * the number of times, we want to execute a block of code:
 * It enables us to initialize the loop varible,
 * check the condition,and increment/decrement in a single line of code;
 * for each loop:
 *Java provides an enhanced for loop to traverse the data structure like array or collection
 *In the for-each loop,we don't need to update the loop varible . 
 *
 */
{
	public static void main(String[] args) {
		
	
 for(int i=1;i<=3;i++)
 {
	 System.out.println(i);
 }
}
}